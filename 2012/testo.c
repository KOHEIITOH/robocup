void user_main(void)
{
   while (TRUE) {
      if (gAD[CN1] < 818) {
         gPwm[0] = 0x64; 
         gPwm[1] = 0x64; 
         gPwm[2] = 0x00; 
         gPwm[3] = 0x00; 
         gPwm[4] = 0x00; 
         gPwm[5] = 0x00; 
         pwm_out();
      } else {
         gPwm[0] = 0x00; 
         gPwm[1] = 0x00; 
         gPwm[2] = 0x00; 
         gPwm[3] = 0x00; 
         gPwm[4] = 0x00; 
         gPwm[5] = 0x00; 
         pwm_out();
      }
   }
}
